/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  //HAL_PWREx_EnableUltraLowPower();

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();

  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  /* Retrieve last supply state from EEPROM */
  /* In case of a total power loss because the SCap was discharged below 1.9V */
  prev_state = load_eePROMqueue();
  //prev_state = readFromEEPROM(EEPROM_BASE_ADDR + STATE_BACKUP_OFFSET);

  /* Restore unit state */
  switch (prev_state)
  {
  	  case APPshtdn_powered:
  	  case APPshtdn_unpowered:
  	  case ON_unpowered:
  	  case ON_powered:
  		  RunMode();
  		  state = ON_powered;
  		  break;
  	  case OFF_unpowered:
  	  case OFF_powered:
  		  LOWSIDE_SW_OPEN();
  		  CHARGER_BOOSTER_OFF();
  		  POWER_GOOD_2RPI_OFF();
  		  state = OFF_powered;
  		  break;
  	  case state_undefined:
  	  default:
  		  RunMode();
  		  state = ON_powered;
  		  break;
  }

  if(state != prev_state) // Minimize eEPROM writes
  {

//	writeToEEPROM(EEPROM_BASE_ADDR + STATE_BACKUP_OFFSET, state);
	write_eePROMvalue(state);
  }

	/* Enable Button, power good IRQ and shutdown acknowledge IRQ */
    HAL_NVIC_EnableIRQ(EXTI0_1_IRQn);
    HAL_NVIC_EnableIRQ(EXTI4_15_IRQn);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    EnterStopMode();

  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  RCC_OscInitTypeDef RCC_OscInitStruct = {0};
  RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

  /** Configure the main internal regulator output voltage 
  */
  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE3);
  /** Initializes the RCC Oscillators according to the specified parameters
  * in the RCC_OscInitTypeDef structure.
  */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_MSI;
  RCC_OscInitStruct.MSIState = RCC_MSI_ON;
  RCC_OscInitStruct.MSICalibrationValue = 0;
  RCC_OscInitStruct.MSIClockRange = RCC_MSIRANGE_5;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }
  /** Initializes the CPU, AHB and APB busses clocks 
  */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_MSI;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0) != HAL_OK)
  {
    Error_Handler();
  }
}

